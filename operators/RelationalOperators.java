package operators;
public class RelationalOperators{
    public static void main(String[] args) {
        int a = 4;
        int b = 5;

        System.out.println("a = " +a + " b = " + b);
        System.out.println("a == b: " + (a==b));
        System.out.println("a != b: " + (a!=b));
        System.out.println("a > b: " + (a>b));
        System.out.println("a >= b: " + (a>=b));
        System.out.println("a < b: " + (a<b));
        System.out.println("a <= b: " + (a<=b));
    }
}